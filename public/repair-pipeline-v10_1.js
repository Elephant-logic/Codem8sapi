/* Codem8s repair pipeline v10.1
 * Loaded after the main app. It narrows AI repairs to failing files,
 * protects deployment/config files, and replaces opaque data-URL errors
 * with useful project filenames where possible.
 */
(() => {
  'use strict';

  const protectedFilePattern = /(^|\/)(?:\.env(?:\.example)?|\.gitignore|render\.ya?ml|package-lock\.json|yarn\.lock|pnpm-lock\.yaml)$/i;
  const isProtectedFile = name => protectedFilePattern.test(String(name || ''));
  const unique = values => [...new Set(values.filter(Boolean))];

  function cleanRuntimeMessage(message) {
    const text = String(message || 'Unknown runtime error');
    if (!/data:text\/javascript/i.test(text)) return text;

    const syntaxCandidates = Object.entries(project.files || {})
      .filter(([name]) => /\.(?:js|mjs|cjs)$/i.test(name))
      .map(([name, content]) => {
        try {
          // sourceURL improves stacks in browsers that expose compile locations.
          new Function(`${content}\n//# sourceURL=codem8s://${name}`);
          return null;
        } catch (error) {
          const stack = String(error?.stack || '');
          const position = stack.match(/codem8s:\/\/[^:\s]+:(\d+):(\d+)/);
          return `${name}: ${error.message}${position ? ` (line ${position[1]}, column ${position[2]})` : ''}`;
        }
      })
      .filter(Boolean);

    if (syntaxCandidates.length === 1) return syntaxCandidates[0];
    if (syntaxCandidates.length > 1) return `JavaScript syntax errors: ${syntaxCandidates.join(' | ')}`;
    return text.replace(/data:text\/javascript[^\s]*/gi, '[bundled script]');
  }

  // Sanitize console output generated by the original app.
  const originalLog = log;
  log = function patchedLog(type, message) {
    return originalLog(type, cleanRuntimeMessage(message));
  };

  function syntaxIssues() {
    const issues = [];
    for (const [name, content] of Object.entries(project.files || {})) {
      if (/\.(?:js|mjs|cjs)$/i.test(name)) {
        try {
          new Function(`${content}\n//# sourceURL=codem8s://${name}`);
        } catch (error) {
          const stack = String(error?.stack || '');
          const position = stack.match(/codem8s:\/\/[^:\s]+:(\d+):(\d+)/);
          issues.push(`${name}: JavaScript syntax error: ${error.message}${position ? ` (line ${position[1]}, column ${position[2]})` : ''}`);
        }
      }
      if (/\.json$/i.test(name)) {
        try { JSON.parse(content); }
        catch (error) { issues.push(`${name}: JSON error: ${error.message}`); }
      }
      if (!isProtectedFile(name) && !String(content).trim()) issues.push(`${name} is empty.`);
    }
    return issues;
  }

  collectIssues = function patchedCollectIssues() {
    saveEditor();
    const issues = [];
    const names = Object.keys(project.files || {});
    if (!names.length) return ['No files found.'];

    const htmlName = typeof chooseEntry === 'function'
      ? chooseEntry()
      : (project.files['index.html'] ? 'index.html' : names.find(name => /\.html?$/i.test(name)));

    if (htmlName) {
      const html = project.files[htmlName] || '';
      const refs = [...html.matchAll(/(?:src|href)=["']([^"']+)["']/gi)]
        .map(match => match[1])
        .filter(ref => !/^(?:https?:|data:|blob:|#|\/\/)/i.test(ref));
      for (const ref of refs) {
        const resolved = normalizePath(htmlName, ref);
        if (project.files[resolved] == null && !assets.some(asset => assetPath(asset) === resolved)) {
          issues.push(`Missing referenced file: ${resolved}`);
        }
      }
    }

    issues.push(...syntaxIssues());
    for (const runtimeError of runtimeErrors || []) issues.push(`Runtime error: ${cleanRuntimeMessage(runtimeError)}`);
    return unique(issues);
  };

  const originalRunTests = runTests;
  runTests = function patchedRunTests(showPane = true) {
    const issues = originalRunTests(false);
    const filtered = unique((issues || []).filter(issue => {
      const emptyMatch = String(issue).match(/^(.+) is empty\.$/);
      return !(emptyMatch && isProtectedFile(emptyMatch[1]));
    }).map(cleanRuntimeMessage));

    if (showPane) switchPane('consolePane');
    status(filtered.length
      ? `Tests found ${filtered.length} issue${filtered.length === 1 ? '' : 's'}.`
      : 'Tests passed.', filtered.length ? 'err' : 'ok');
    return filtered;
  };

  function issueFilenames(issues) {
    const names = Object.keys(project.files || {});
    const selected = new Set();
    for (const issue of issues || []) {
      const text = String(issue);
      for (const name of names) {
        if (!isProtectedFile(name) && text.includes(name)) selected.add(name);
      }
      const missing = text.match(/Missing referenced file:\s*(.+)$/i)?.[1]?.trim();
      if (missing && project.files[missing] != null && !isProtectedFile(missing)) selected.add(missing);
    }

    // Include an entry HTML file as context when repairing browser code.
    const entry = typeof chooseEntry === 'function' ? chooseEntry() : null;
    if (entry && !isProtectedFile(entry)) selected.add(entry);

    // If diagnostics did not identify a file, include only editable source files.
    if (!selected.size) {
      names.filter(name => /\.(?:html?|css|js|mjs|cjs|json|ts|tsx|jsx)$/i.test(name) && !isProtectedFile(name))
        .slice(0, 8)
        .forEach(name => selected.add(name));
    }
    return [...selected];
  }

  repairProject = async function scopedRepairProject(issues, attempt, total, regressionNotes = []) {
    if (els.snapshots.checked) snapshotProject(`Before repair ${attempt}`);
    setStage(4);
    status(`Repair attempt ${attempt}/${total}…`);

    const selectedNames = issueFilenames(issues);
    const selectedFiles = Object.fromEntries(selectedNames.map(name => [name, project.files[name]]));
    log('warn', `Sending ${issues.length} detected issue${issues.length === 1 ? '' : 's'} for repair.`);
    log('info', `Repair scope: ${selectedNames.join(', ') || 'no matching source files'}`);

    const regressionText = regressionNotes.length
      ? `\n\nREGRESSIONS FROM A REJECTED REPAIR — DO NOT REPEAT:\n- ${regressionNotes.join('\n- ')}`
      : '';

    const instructions = [
      'Return ONLY strict JSON in this shape: {"files":{"path/file.ext":"complete repaired content"}}.',
      'Return only files that genuinely need changing.',
      'Make the smallest possible repair.',
      'Do not rename files unless the issue explicitly requires it.',
      'Do not modify or return .env, .env.example, .gitignore, render.yaml, package lockfiles, or dependency lockfiles.',
      'Preserve all unrelated behavior and previously passing checks.',
      'Do not include markdown fences or commentary.'
    ].join(' ');

    const input = `Repair the listed issues.\n\nISSUES:\n- ${(issues || []).join('\n- ')}${regressionText}\n\nFILES IN REPAIR SCOPE:\n${JSON.stringify(selectedFiles)}`;
    const response = await fetch('/api/openai', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ model: els.model.value, instructions, input })
    });
    const data = await response.json();
    if (!response.ok) throw new Error(data?.error?.message || 'Repair request failed');

    const text = extractText(data);
    let parsed;
    try { parsed = JSON.parse(text.replace(/^```(?:json)?\s*/i, '').replace(/```\s*$/i, '').trim()); }
    catch { throw new Error('Repair response was not valid JSON.'); }

    const returnedFiles = parsed?.files && typeof parsed.files === 'object' ? parsed.files : {};
    const accepted = [];
    for (const [name, content] of Object.entries(returnedFiles)) {
      if (isProtectedFile(name)) {
        log('warn', `Ignored protected file returned by repair: ${name}`);
        continue;
      }
      if (typeof content !== 'string') continue;
      project.files[name] = content;
      accepted.push(name);
    }
    if (!accepted.length) throw new Error('Repair returned no usable source-file changes.');

    currentFile = project.files[currentFile] != null
      ? currentFile
      : (typeof chooseEntry === 'function' ? chooseEntry() : accepted[0]);
    renderFiles();
    renderPreview();
    setStage(5);
    switchPane('previewPane');
    log('info', `Merged repaired files: ${accepted.join(', ')}`);
    await new Promise(resolve => setTimeout(resolve, 1200));
  };

  // Prevent protected config files being treated as regressions merely because
  // an AI candidate omitted them from a partial repair response.
  if (typeof findRegressions === 'function') {
    const originalFindRegressions = findRegressions;
    findRegressions = function patchedFindRegressions(beforePassing, afterState) {
      return originalFindRegressions(beforePassing, afterState).filter(id => {
        const match = String(id).match(/^file:(.+):nonempty$/);
        return !(match && isProtectedFile(match[1]));
      });
    };
  }

  log('ok', 'Repair pipeline v10.1 active: scoped repairs, protected config files, and clearer runtime diagnostics.');
})();
